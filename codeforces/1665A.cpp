// A. GCD vs LCM

#include <iostream>


int main() {

    std::ios_base::sync_with_stdio(false);
    std::cin.tie(NULL);

    int t;
    std::cin >> t;

    while (t--) {

        int n;
        std::cin >> n;

        std::cout << n - 3 << ' ' << 1 << ' ' << 1 << ' ' << 1 << "\n";
    }

    return 0;
}
