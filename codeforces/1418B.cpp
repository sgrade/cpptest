using namespace std;


int main(){

    int t;
    scanf("%d\n", &t);

    while (t--){

        int n;
        int a[n];
        int l[n];
        for (int i=0; i<n; ++i){
            scanf("%d", &(a[i]));
        }
        scanf("\n");
        for (int i=0; i<n; ++i){
            scanf("%d", &(l[i]));
        }
        scanf("\n");

        

    }

    return 0;
}